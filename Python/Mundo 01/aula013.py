# Transformação de strings
frase = 'Curso em Video Python'

print(frase.replace('Python', 'Android'))  # Substitui o primeiro parâmetro pelo segundo
print(frase.upper())  # Deixa toda a string em maiúsculas
print(frase.lower())  # Deixa roda a string em minúsculas
print(frase.capitalize())  # Deixa toda a string em minúsculas e a primeira letra fica maiúscula
print(frase.title())  # Deixa todas as palavras da string com a primeira letra maiúscula
